# 運算
# 運算符
a = 10
b = 3
print(a//b) # 取整數
print(a ** 3)# 冪運算
# 比較運算
print("Hello" < "Hi") # 比較asci，只比較到第二位
# 邏輯運算
print(1 and 0)
print(1 or 0)
print(not 1)
# 位運算符
print(5 ^ 7)# 位異或
print(~(-7))# 取反 # 二進制兩位 : 正負號以及一位 # 原碼+1變負數
# 成員運算符
print(3 in (1, 2, 3))
print(4 not in (1, 2, 3))
print(3 is 4)
print(3 is not 4)
